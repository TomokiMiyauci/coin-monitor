export const minValue = (values: number[]): number => Math.min(...values)
export const maxValue = (values: number[]): number => Math.max(...values)
export const add = (value: number, value2: number): number => value + value2
export const subtract = (value: number, value2: number): number =>
  value - value2
export const divide = (value: number, value2: number): number => value / value2
